from django.contrib.contenttypes.models import ContentType
import factory

from inboxen.cms import models


class HelpBasePageFactory(factory.django.DjangoModelFactory):
    class Meta:
        model = models.HelpBasePage

    content_type = factory.Iterator(ContentType.objects.filter(app_label="cms", model="helpbasepage"))
